def extract_bit_width(text):
    """Extract bit width from description"""
    text = text.lower()
    for width in [64, 32, 16, 8, 4]:
        if f"{width}-bit" in text or f"{width} bit" in text:
            return width
    words = text.split()
    for i, word in enumerate(words):
        if 'bit' in word and i > 0 and words[i - 1].isdigit():
            return int(words[i - 1])
    return None


