def extract_code_from_markdown(text):
    """Extract code from markdown code blocks"""
    lines = text.split('\n')
    in_code_block = False
    code_lines = []
    language = None

    for line in lines:
        if line.startswith('```'):
            if not in_code_block:
                language = line[3:].strip() or "unknown"
                in_code_block = True
            else:
                in_code_block = False
        elif in_code_block:
            code_lines.append(line)

    return '\n'.join(code_lines), language


