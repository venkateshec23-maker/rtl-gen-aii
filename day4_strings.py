def build_verilog_module(module_name, inputs, outputs):
    """Build Verilog module from specifications"""
    lines = []
    lines.append(f"// {module_name}.v")
    lines.append("// Generated by RTL-Gen AII")
    lines.append("")
    lines.append(f"module {module_name}(")

    port_lines = []
    for n, width in inputs:
        if width > 1:
            port_lines.append(f"    input [{width-1}:0] {n}")
        else:
            port_lines.append(f"    input {n}")

    for n, width in outputs:
        if width > 1:
            port_lines.append(f"    output [{width-1}:0] {n}")
        else:
            port_lines.append(f"    output {n}")

    for i, line in enumerate(port_lines):
        if i < len(port_lines) - 1:
            lines.append(line + ",")
        else:
            lines.append(line)

    lines.append(");")
    lines.append("")
    lines.append("    // TODO: Add implementation")
    lines.append("")
    lines.append("endmodule")
    return '\n'.join(lines)


