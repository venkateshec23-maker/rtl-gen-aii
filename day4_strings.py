def parse_design_description(description):
    """Extract design information from natural language"""
    desc = description.lower()
    result = {
        "original": description,
        "component": "unknown",
        "bit_width": 8,
        "has_clock": False,
        "has_reset": False,
        "operations": []
    }

    components = {
        "adder": ["adder", "add"],
        "counter": ["counter", "count"],
        "alu": ["alu", "arithmetic"],
        "multiplexer": ["mux", "multiplexer"],
        "register": ["register", "reg", "flip-flop"]
    }

    for comp, keywords in components.items():
        for kw in keywords:
            if kw in desc:
                result["component"] = comp
                break
        if result["component"] != "unknown":
            break

    width_patterns = [
        r'(\d+)[-\s]bit',
        r'bit[-\s]width[:\s](\d+)',
    ]

    for pattern in width_patterns:
        match = re.search(pattern, desc)
        if match:
            result["bit_width"] = int(match.group(1))
            break

    result["has_clock"] = any(word in desc for word in ["clock", "clk"])
    result["has_reset"] = any(word in desc for word in ["reset", "rst"])

    ops = ["add", "sub", "and", "or", "xor", "not"]
    for op in ops:
        if op in desc:
            result["operations"].append(op.upper())

    return result


