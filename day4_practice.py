template = """// {{module_name}}.v
// Bit width: {{bit_width}}

module {{module_name}}(
    input [{{bit_width_minus_1}}:0] a,
    input [{{bit_width_minus_1}}:0] b,
    output [{{bit_width_minus_1}}:0] sum
);
    assign sum = a + b;
endmodule
"""

with open("template.v", "w") as f:
    f.write(template)


def generate_from_template(template_file, replacements, output_file):
    """Generate file from template"""
    with open(template_file, 'r') as f:
        content = f.read()

    for key, value in replacements.items():
        content = content.replace("{{" + key + "}}", str(value))

    with open(output_file, 'w') as f:
        f.write(content)
    print(f"✓ Generated {output_file} from template")


replacements = {
    "module_name": "adder_8bit",
    "bit_width": "8",
    "bit_width_minus_1": "7"
}

generate_from_template("template.v", replacements, "adder_8bit_from_template.v")

with open("adder_8bit_from_template.v", 'r') as f:
    print("\nGenerated from template:")
    print(f.read())
