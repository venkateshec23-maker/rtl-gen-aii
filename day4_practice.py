# EXERCISE 2: Design Description Parser
# ============================================


def parse_and_save(description, output_file):
    """Parse description and save to JSON"""
    import re
    desc = description.lower()

    comp = "unknown"
    for c in ["adder", "counter", "alu", "mux"]:
        if c in desc:
            comp = c
            break

    width = 8
    match = re.search(r'(\d+)[-\s]bit', desc)
    if match:
        width = int(match.group(1))

    result = {
        "description": description,
        "component": comp,
        "bit_width": width,
        "has_clock": "clock" in desc or "clk" in desc,
        "has_reset": "reset" in desc or "rst" in desc
    }

    with open(output_file, 'w') as f:
        json.dump(result, f, indent=2)

    print(f"Saved parsed design to {output_file}")
    return result


parse_and_save("Create a 16-bit ALU with ADD, SUB", "design.json")

