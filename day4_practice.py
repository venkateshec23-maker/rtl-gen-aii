# EXERCISE 3: Multi-File Generator
# ============================================


def generate_adder_family():
    """Generate adders for 4, 8, 16 bits"""
    bit_widths = [4, 8, 16]

    os.makedirs("outputs", exist_ok=True)

    for width in bit_widths:
        code = f"""// adder_{width}bit.v
// Generated by RTL-Gen AII

module adder_{width}bit(
    input [{width-1}:0] a,
    input [{width-1}:0] b,
    output [{width-1}:0] sum,
    output carry
);
    assign {{carry, sum}} = a + b;
endmodule
"""
        filename = os.path.join("outputs", f"adder_{width}bit.v")
        with open(filename, 'w') as f:
            f.write(code)
        print(f"✓ Generated {filename}")


generate_adder_family()

