from typing import Dict


def get_combinational_template() -> str:
    """
    Template for combinational circuit testbench.
    
    Variables:
    - {module_name}: Name of module under test
    - {tb_name}: Testbench module name
    - {signal_declarations}: reg/wire declarations
    - {dut_instantiation}: DUT instantiation
    - {test_cases}: Test case code
    """
    return """//==============================================================================
// Testbench: {tb_name}
// Auto-generated by RTL-Gen AI
// Type: Combinational
//==============================================================================

`timescale 1ns/1ps

module {tb_name};

    // Signal declarations
{signal_declarations}
    
    // Instantiate DUT
    {module_name} dut(
{dut_ports}
    );
    
    // Test variables
    integer tests_passed = 0;
    integer tests_failed = 0;
    
    // Test procedure
    initial begin
        $dumpfile("waveform.vcd");
        $dumpvars(0, {tb_name});
        
        $display("Testing {module_name}...");
        $display("");
        
{test_cases}
        
        // Summary
        $display("");
        $display("======================================");
        $display("Test Summary:");
        $display("  Passed: %0d", tests_passed);
        $display("  Failed: %0d", tests_failed);
        $display("======================================");
        
        if (tests_failed == 0) begin
            $display("All tests passed!");
        end else begin
            $display("FAILED: %0d test(s) failed", tests_failed);
        end
        
        $finish;
    end

endmodule
"""


