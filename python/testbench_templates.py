def get_clock_generation(clock_period: int = 10) -> str:
    """
    Generate clock generation code.
    
    Args:
        clock_period: Clock period in time units
        
    Returns:
        str: Verilog code for clock generation
    """
    half_period = clock_period // 2
    return f"""    // Clock generation (period = {clock_period}ns)
    initial begin
        clk = 0;
        forever #{half_period} clk = ~clk;
    end
"""


def get_reset_sequence(reset_name: str = 'reset', active_high: bool = True) -> str:
    """
    Generate reset sequence code.
    
    Args:
        reset_name: Name of reset signal
        active_high: True if reset is active high
        
    Returns:
        str: Verilog code for reset sequence
    """
    active = '1' if active_high else '0'
    inactive = '0' if active_high else '1'
    
    return f"""        // Apply reset
        {reset_name} = {active};
        #20;
        {reset_name} = {inactive};
        #10;
"""


