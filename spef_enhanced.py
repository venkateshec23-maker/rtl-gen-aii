@dataclass
class RealParasiticNet:
    net_name: str = ""
    resistance_ohm: float = 0.0
    capacitance_pf: float = 0.0
    coupling_cap_pf: float = 0.0
    wire_length_um: float = 0.0
    delay_impact_ps: float = 0.0
    slew_degradation_pct: float = 0.0

    def total_cap_pf(self) -> float:
        return self.capacitance_pf + self.coupling_cap_pf


@dataclass
class RealSPEFResult:
    design_name: str = ""
    total_nets: int = 0
    total_wire_length_um: float = 0.0
    total_resistance_ohm: float = 0.0
    total_capacitance_pf: float = 0.0
    total_coupling_cap_pf: float = 0.0
    nets: List[RealParasiticNet] = field(default_factory=list)
    spef_file: str = ""
    extracted_at: str = ""
    engine: str = "openrcx"

    def top_rc_nets(self, n: int = 10) -> List[RealParasiticNet]:
        return sorted(self.nets, key=lambda x: x.delay_impact_ps, reverse=True)[:n]

    def worst_coupling_nets(self, n: int = 10) -> List[RealParasiticNet]:
        return sorted(self.nets, key=lambda x: x.coupling_cap_pf, reverse=True)[:n]

    def avg_resistance(self) -> float:
        return (self.total_resistance_ohm / self.total_nets) if self.total_nets else 0.0

    def summary_dict(self) -> Dict:
        return {
            "design_name": self.design_name,
            "total_nets": self.total_nets,
            "total_wire_length_um": round(self.total_wire_length_um, 2),
            "total_resistance_ohm": round(self.total_resistance_ohm, 2),
            "total_capacitance_pf": round(self.total_capacitance_pf, 4),
            "total_coupling_cap_pf": round(self.total_coupling_cap_pf, 4),
            "engine": self.engine,
            "spef_file": self.spef_file,
            "extracted_at": self.extracted_at,
            "top_rc_nets": [
                {"net": n.net_name, "r": round(n.resistance_ohm, 2),
                 "c": round(n.total_cap_pf(), 4), "delay_ps": round(n.delay_impact_ps, 2)}
                for n in self.top_rc_nets(10)
            ],
        }

    def to_spef(self) -> str:
        lines = [
            "*SPEF",
            "*DESIGN " + self.design_name,
            "*DATE " + self.extracted_at,
            "*VENDOR RTL-Gen AI",
            "*PROGRAM spef_enhanced.py",
            "*DIVIDER /",
            "*DELIMITER :",
            "*BUS_DELIMITER []",
            "*T_UNIT 1 NS",
            "*C_UNIT 1 PF",
            "*R_UNIT 1 OHM",
            "*L_UNIT 1 UM",
            "",
            f"*DESIGN_CAP {self.total_capacitance_pf:.6f}",
            f"*DESIGN_RES {self.total_resistance_ohm:.2f}",
            f"*DESIGN_COUPLING_CAP {self.total_coupling_cap_pf:.6f}",
            "",
        ]
        for i, net in enumerate(self.nets[:1000]):
            total_c = net.total_cap_pf()
            lines.append(f"*D_NET {net.net_name} {total_c:.6f}")
            lines.append(f"*RES 1")
            lines.append(f"1 *P {i} *P {i + 1} {net.resistance_ohm:.2f}")
            lines.append(f"*CAP 2")
            lines.append(f"1 *P {i} {net.capacitance_pf:.6f}")
            lines.append(f"2 *P {i} {net.coupling_cap_pf:.6f} *C")
            lines.append(f"*END")
        return "\n".join(lines)


