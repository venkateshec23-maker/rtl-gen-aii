# ── Real SPEF parser ─────────────────────────────────────────────────────────

def parse_real_spef(spef_path: Path) -> RealSPEFResult:
    """
    Parse an actual SPEF file generated by OpenRCX.
    Returns RealSPEFResult with net-level parasitics.
    """
    if not spef_path.exists():
        log.warning("SPEF file not found: %s", spef_path)
        return RealSPEFResult()

    text = spef_path.read_text(errors="ignore")
    result = RealSPEFResult(spef_file=str(spef_path))
    result.extracted_at = datetime.now().isoformat()

    # Extract design name
    m = re.search(r'\*DESIGN\s+(\S+)', text)
    if m:
        result.design_name = m.group(1)

    # Extract per-net parasitics
    # SPEF format: *D_NET <name> <total_cap>
    #              *RES <count>
    #              <idx> *P <a> *P <b> <res>
    #              *CAP <count>
    #              <idx> *P <node> <cap> [*C]
    #              *END
    # Note: *D_NET's total_cap already includes all individual caps.
    # Individual *CAP entries only add to coupling when marked with *C.
    current_net = None
    for line in text.splitlines():
        line = line.strip()
        m = re.match(r'\*D_NET\s+(\S+)\s+([\d.eE+-]+)', line)
        if m:
            current_net = RealParasiticNet(
                net_name=m.group(1),
                capacitance_pf=float(m.group(2)),
            )
            result.nets.append(current_net)
            continue
        if current_net and line.startswith('*RES'):
            continue
        if current_net and line.startswith('*CAP'):
            continue
        if current_net and line.startswith('*END'):
            # Aggregate into totals
            result.total_resistance_ohm += current_net.resistance_ohm
            result.total_capacitance_pf += current_net.capacitance_pf
            result.total_coupling_cap_pf += current_net.coupling_cap_pf

            # Estimate delay impact (Elmore delay ~ R * C)
            current_net.delay_impact_ps = (
                current_net.resistance_ohm * current_net.total_cap_pf() * 1e3
            )
            current_net = None
            continue
        if current_net:
            # Resistance line: <idx> *P <a> *P <b> <res>
            m = re.match(r'\d+\s+\*P\s+\S+\s+\*P\s+\S+\s+([\d.eE+-]+)', line)
            if m:
                current_net.resistance_ohm += float(m.group(1))
            # Capacitance line with coupling marker: <idx> *P <node> <cap> *C
            # Only coupling caps add separately; *D_NET already includes total cap
            m = re.match(r'\d+\s+\*P\s+\S+\s+([\d.eE+-]+)\s+\*C', line)
            if m:
                current_net.coupling_cap_pf += float(m.group(1))

    result.total_nets = len(result.nets)
    return result


def parse_parasitic_report(report_path: Path) -> RealSPEFResult:
    """
    Parse the detailed parasitic report generated by the OpenROAD script.
    Falls back to SPEF file if report not available.
    """
    if not report_path.exists():
        return RealSPEFResult()

    text = report_path.read_text(errors="ignore")
    result = RealSPEFResult()
    result.extracted_at = datetime.now().isoformat()

    m = re.search(r"Design:\s*(\S+)", text)
    if m:
        result.design_name = m.group(1)

    # Parse per-net lines: "Net: <name> | R=<r> ohm | C=<c> pF | Cc=<cc> pF"
    net_pattern = re.compile(
        r"Net:\s*(\S+)\s*\|\s*R=([\d.eE+-]+)\s+ohm\s*\|\s*"
        r"C=([\d.eE+-]+)\s+pF\s*\|\s*Cc=([\d.eE+-]+)\s+pF"
    )
    for m in net_pattern.finditer(text):
        pn = RealParasiticNet(
            net_name=m.group(1),
            resistance_ohm=float(m.group(2)),
            capacitance_pf=float(m.group(3)),
            coupling_cap_pf=float(m.group(4)),
        )
        pn.delay_impact_ps = pn.resistance_ohm * pn.total_cap_pf() * 1e3
        result.nets.append(pn)

    # Aggregate totals
    m = re.search(r"Total resistance:\s*([\d.eE+-]+)\s+ohm", text)
    if m:
        result.total_resistance_ohm = float(m.group(1))
    m = re.search(r"Total capacitance:\s*([\d.eE+-]+)\s+pF", text)
    if m:
        result.total_capacitance_pf = float(m.group(1))
    m = re.search(r"Total coupling cap:\s*([\d.eE+-]+)\s+pF", text)
    if m:
        result.total_coupling_cap_pf = float(m.group(1))

    result.total_nets = len(result.nets)
    result.engine = "openrcx"

    if result.total_nets == 0:
        log.warning("No nets parsed from parasitic report; may need SPEF file")

    return result


def extract_real_spef(
    design_name: str,
    routed_def: str,
    results_dir: str,
    docker_manager=None,
    tlef: Optional[str] = None,
    lef: Optional[str] = None,
    liberty: Optional[str] = None,
    sdc: Optional[str] = None,
) -> RealSPEFResult:
    """
    Run real SPEF extraction via OpenROAD/OpenRCX (Docker).
    Falls back to report parsing if Docker unavailable.

    Args:
        design_name: Design name (e.g. "adder_8bit")
        routed_def: Path to routed.def
        results_dir: Output directory for SPEF and reports
        docker_manager: DockerManager instance for container execution
        tlef, lef, liberty, sdc: PDK file paths inside container

    Returns:
        RealSPEFResult with extracted parasitics
    """
    rd = Path(results_dir)
    spef_path = rd / f"{design_name}.spef"
    report_path = rd / "parasitic_report.txt"
    netlist_path = rd / f"{design_name}_routed_post_spef.v"

    # If SPEF already exists, parse it
    if spef_path.exists():
        log.info("SPEF file found: %s", spef_path)
        result = parse_real_spef(spef_path)
        if result.total_nets > 0:
            return result

    # If parasitic report exists, parse it
    if report_path.exists():
        log.info("Parasitic report found: %s", report_path)
        result = parse_parasitic_report(report_path)
        if result.total_nets > 0:
            return result

    # If Docker is available, run extraction
    if docker_manager:
        log.info("Running real OpenRCX extraction via Docker...")
        script = generate_spef_extraction_script(
            design_name=design_name,
            tlef=tlef,
            lef=lef,
            liberty=liberty,
            routed_def=routed_def,
            sdc=sdc,
            output_spef=str(spef_path).replace("\\", "/"),
            output_report=str(report_path).replace("\\", "/"),
            output_netlist=str(netlist_path).replace("\\", "/"),
        )

        script_container = "/work/scripts/spef_extraction.tcl"
        docker_manager.run_command(
            f"mkdir -p /work/scripts && cat > {script_container} << 'SPEFEOF'\n{script}\nSPEFEOF"
        )
        rc, out, err = docker_manager.run_script(
            script_container, interpreter="openroad -exit",
            timeout=600, log_file="spef_extraction.log"
        )

        # Parse generated SPEF
        if spef_path.exists():
            result = parse_real_spef(spef_path)
            result.design_name = design_name
            return result

        # Fall back to report
        if report_path.exists():
            result = parse_parasitic_report(report_path)
            result.design_name = design_name
            return result

    # Last resort: return empty result
    log.warning("No SPEF extraction possible; returning empty result")
    return RealSPEFResult(design_name=design_name)


# ── Visualizations ───────────────────────────────────────────────────────────

def plot_rc_histogram(result: RealSPEFResult):
    """Plot R and C histograms from real SPEF extraction."""
    if not result.nets:
        return go.Figure()

    fig = go.Figure()
    fig.add_trace(go.Histogram(
        x=[n.resistance_ohm for n in result.nets],
        name="Resistance (ohm)",
        opacity=0.7,
        nbinsx=30,
        marker_color="#4fc3f7",
    ))
    fig.add_trace(go.Histogram(
        x=[n.total_cap_pf() * 1e3 for n in result.nets],
        name="Capacitance (fF)",
        opacity=0.7,
        nbinsx=30,
        marker_color="#ff9800",
        xaxis="x2",
    ))

    fig.update_layout(
        title="OpenRCX Parasitic Distribution",
        xaxis=dict(title="Resistance (ohm)", domain=[0, 0.45]),
        xaxis2=dict(title="Capacitance (fF)", domain=[0.55, 1]),
        yaxis=dict(title="Count"),
        barmode="overlay",
        template="plotly_dark",
        height=400,
    )
    return fig


def plot_top_rc_nets(result: RealSPEFResult, n: int = 15):
    """Plot top RC nets by delay impact."""
    top = result.top_rc_nets(n)
    if not top:
        return go.Figure()

    fig = go.Figure()
    fig.add_trace(go.Bar(
        x=[n.net_name for n in top],
        y=[n.delay_impact_ps for n in top],
        marker_color="#ef5350",
        name="Delay Impact (ps)",
    ))

    fig.update_layout(
        title=f"Top {n} Nets by RC Delay Impact",
        xaxis=dict(title="Net", tickangle=45),
        yaxis=dict(title="Delay Impact (ps)"),
        template="plotly_dark",
        height=400,
    )
    return fig


